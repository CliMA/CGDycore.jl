function QuadRule(type::Grids.QuadPrimal,n)
  NumQuad = n * n
  x, w = gaussradau(n)
  Weights = zeros(NumQuad)
  Points = zeros(NumQuad,2)
  ii = 1
  for j = 1 : n
    for i = 1 : n
      Weights[ii] = w[i] * w[j]
      Points[ii,1] = x[i]
      Points[ii,2] = x[j]
      ii += 1
    end
  end
  return NumQuad, Weights, Points
end


function QuadRule(type::Grids.QuadDual,n)
  NumQuad =  n * n  
  x, w = gaussradau(n)
  x .= -x
  Weights = zeros(NumQuad)
  Points = zeros(NumQuad,2)
  ii = 1
  for j = 1 : n
    for i = 1 : n
      Weights[ii] = w[n+1-i] * w[n+1-j]
      Points[ii,1] = x[i]
      Points[ii,2] = x[j]
      ii += 1
    end
  end
  return NumQuad, Weights, Points
end

